package com.example.demo.mybatis_Interceptor;

import org.apache.ibatis.executor.Executor;
import org.apache.ibatis.mapping.BoundSql;
import org.apache.ibatis.mapping.MappedStatement;
import org.apache.ibatis.mapping.SqlCommandType;
import org.apache.ibatis.plugin.*;
import org.apache.ibatis.session.Configuration;
import org.apache.ibatis.session.ResultHandler;
import org.apache.ibatis.session.RowBounds;
import org.apache.logging.log4j.LogManager;
import org.apache.logging.log4j.Logger;
import org.springframework.stereotype.Component;

import java.util.Properties;

@Intercepts({
        @Signature(type = Executor.class, method = "update", args = {MappedStatement.class, Object.class}),
        @Signature(type = Executor.class, method = "query", args = {MappedStatement.class, Object.class, RowBounds.class, ResultHandler.class})})
@Component
public class ExecutorInterceptor implements Interceptor {
    private static final Logger logger = LogManager.getLogger(ExecutorInterceptor.class);


    @Override
    public Object intercept(Invocation invocation) throws Throwable {
        //logger.info("================================mybatis 拦截执行器................................start");
        // 1. 获取MappedStatement实例, 并获取当前SQL命令类型
        MappedStatement ms = (MappedStatement) invocation.getArgs()[0];
        SqlCommandType commandType = ms.getSqlCommandType();
        // 2. 获取当前正在被操作的类, 有可能是Java Bean, 也可能是普通的操作对象, 比如普通的参数传递
        // 普通参数, 即是 @Param 包装或者原始 Map 对象, 普通参数会被 Mybatis 包装成 Map 对象
        // 即是 org.apache.ibatis.binding.MapperMethod$ParamMap
        Object parameter = invocation.getArgs()[1];
        // 获取拦截器指定的方法类型, 通常需要拦截 update
        String methodName = invocation.getMethod().getName();
        //Configuration configuration = ms.getConfiguration();
        //BoundSql boundSql = ms.getBoundSql(parameter);
        logger.info("================================mybatis 拦截执行器.... methodName; {}, commandType: {},parameter: {}", methodName, commandType);
        //logger.info("mybatis 拦截执行器...boundSql: {}", boundSql.getSql());
        //logger.info("mybatis 拦截执行器...parameter: {}", parameter);
        //logger.info("================================mybatis 拦截执行器................................end");
        return invocation.proceed();
    }

    @Override
    public Object plugin(Object o) {
        return Plugin.wrap(o, this);
    }

    @Override
    public void setProperties(Properties properties) {

    }
}